const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String, // URL string for a profile picture
        default: ""
    }
}, { timestamps: true }); // Automatically logs when a user registers

module.exports = mongoose.model('User', UserSchema);